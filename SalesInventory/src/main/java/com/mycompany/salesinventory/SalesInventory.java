    private void addItemPurchase(){
        String itemName = jComboBox1.getSelectedItem().toString();
        
        try{
            BufferedReader rdfile = new BufferedReader(new FileReader("items.txt"));
            DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
            jTable2.setDefaultEditor(Object.class, null);
                
            jTable2.setRowHeight(30);
            Object[] tableLines = rdfile.lines().toArray();
            
            
            if("".equals(jTextField1.getText()) ){
                JOptionPane.showMessageDialog(null, "Enter Quantity!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                for(Object tableLine : tableLines){
                    String line = tableLine.toString().trim();
                    String[] dataRow = line.split("/");
                    if(itemName.equals(dataRow[0])) {
                        Double itemPrice = Double.valueOf(dataRow[2]);
                        String qty = jTextField1.getText();
                        int itemQuantity = Integer.parseInt(qty);
                        
                        totalQty = totalQty + itemQuantity;
                        
                        Double tempPrice = itemQuantity * itemPrice;
                        
                        totalAmount = totalAmount + tempPrice;
                                
                        String finalItemPrice = tempPrice.toString();
                        model.addRow(new Object[]{itemName, qty, finalItemPrice});
                        jTextArea1.setText(jTextArea1.getText()+itemName.substring(0,5)+"..."+" x"+itemQuantity+" \t\t P"+finalItemPrice+"\n");
                    }
                    else {
                    }    
                }
            }
  
        }catch(IOException e){}
        

    }

    
