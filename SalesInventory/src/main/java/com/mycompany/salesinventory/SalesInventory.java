    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        String item = model.getValueAt(jTable2.getSelectedRow(), 0).toString();
        int minusQty = Integer.parseInt((String) model.getValueAt(jTable2.getSelectedRow(), 1));
        totalQty = totalQty - minusQty;
        
        double minusPrice = Double.valueOf((String) model.getValueAt(jTable2.getSelectedRow(), 2))*minusQty;
        
        totalAmount = totalAmount - minusPrice;
        
        int i;
        int row = model.getRowCount();
        for(i = 0; i < row;i++){
            String dataItem = model.getValueAt(i, 0).toString();
            if(item.equals(dataItem)){
                break;
            }
        }
        model.removeRow(i);
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String strCash = jTextField2.getText();
        Double cash = Double.valueOf(strCash);
        
        jLabel7.setText("PHP " + totalAmount);
        jLabel8.setText("PHP " + cash.toString());
        
        Double change = cash - totalAmount;
        if(change < 0){
            
            jTextArea1.setText("");
            jTextField1.setText("");
            jTextField2.setText("");

            jLabel7.setText("");
            jLabel8.setText("");
            jLabel9.setText("");
            jLabel10.setText("");
            jLabel11.setText("");
            jLabel17.setText("");
            jLabel30.setText("");
            JOptionPane.showMessageDialog(null, "Insufficient Cash!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            jLabel9.setText("PHP " + change.toString());
            DecimalFormat df_vatS = new DecimalFormat("#.##");
            Double vatAmt = totalAmount * 0.12;
            Double vatSales = totalAmount - vatAmt;
            String saleVat = df_vatS.format(vatSales);
            jLabel10.setText("PHP " + saleVat);      
            jLabel11.setText("PHP " + vatAmt.toString());
            jLabel17.setText("PHP " + totalAmount);

            jLabel30.setText("No. of items purchased: " + totalQty);
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

