    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        //edit button
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        String name = jTextField13.getText();
        String qty = jTextField15.getText();
        String price = jTextField14.getText();
        
        model.setValueAt(name, jTable1.getSelectedRow(), 0);
        model.setValueAt(qty, jTable1.getSelectedRow(), 1);
        model.setValueAt(price, jTable1.getSelectedRow(), 2);
        
        try{
            FileWriter fwrite = new FileWriter("items.txt");
            String value;
            for(int row = 0; row < jTable1.getRowCount(); row++){
                for(int col = 0; col < jTable1.getColumnCount(); col++){
                    if(col==2){
                        value = (String) jTable1.getValueAt(row,col) + "\n";
                    }
                    else{
                        value = (String) jTable1.getValueAt(row,col)+ "/";
                            
                    }
                    fwrite.write(value);
                }
            }
            fwrite.close();
            
        }catch(IOException e){}
        
        JOptionPane.showMessageDialog(null, "Edit Success", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton17ActionPerformed

